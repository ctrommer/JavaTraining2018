package land;

public class Hauptstadt {
	private int hoehe;

	public Hauptstadt(int hoehe) {
		super();
		this.hoehe = hoehe;
	}

	public Hauptstadt() {
		super();
	}

	public int getHoehe() {
		return hoehe;
	}

	public void setHoehe(int hoehe) {
		this.hoehe = hoehe;
	}

}
